    private void loadData() {
        List<Transaksi> list = controller.getAllTransaksi();
        dompet.setDaftarTransaksi(list);
        
        labelSaldo.setText("TOTAL SALDO: Rp " + dompet.hitungSaldo());

        DefaultTableModel model = (DefaultTableModel) tblTransaksi.getModel();
        model.setRowCount(0); 
        
        for (Transaksi t : list) {
            Object[] row = {
                t.getId(),
                t.getTanggal(),
                t.getJenisTransaksi(),
                t.getKategori(),
                t.getNominal(),
                t.getKeterangan()
            };
            model.addRow(row);
        }
    }
    
