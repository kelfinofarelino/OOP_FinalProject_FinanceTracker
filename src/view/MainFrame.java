    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {
            Transaksi t;
            String jenis = cbJenis.getSelectedItem().toString();
            
            if (jenis.equals("Pemasukan")) {
                t = new Pemasukan();
            } else {
                t = new Pengeluaran();
            }

            t.setNominal(Double.parseDouble(txtNominal.getText()));
            t.setKategori(cbKategori.getSelectedItem().toString());
            t.setKeterangan(txtKeterangan.getText());
            t.setTanggal(new java.sql.Date(System.currentTimeMillis()));

            controller.simpanTransaksi(t); 
            JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan!");
            loadData(); 
            
            // Mengosongkan form setelah simpan
            txtNominal.setText("");
            txtKeterangan.setText("");
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Nominal harus berupa angka!");
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage()); 
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi Kesalahan: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int selectedRow = tblTransaksi.getSelectedRow();
        if (selectedRow != -1) {
            int id = (int) tblTransaksi.getValueAt(selectedRow, 0);
            try {
                controller.hapusTransaksi(id);
                JOptionPane.showMessageDialog(this, "Data Terhapus!");
                loadData();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Gagal Hapus: " + e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Pilih data di tabel dulu!");
        }
    }//GEN-LAST:event_btnHapusActionPerformed

